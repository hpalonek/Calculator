package pl.edu.agh.mwo.calculator;

public class Calculator {
    public int sum(int i, int i1) {
        return i + i1;
    }

    public int subtract(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i * i1;
    }

    public double divide(int i, int i1) {
        return i / i1;
    }

    public double avg(int i, int i1) {
        return (i + i1)/2;
    }
}
